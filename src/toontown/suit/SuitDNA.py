def getSuitBodyType(name):
    if name in SuitGlobals.suitProperties:
        bodyType = SuitGlobals.suitProperties[name][SuitGlobals.BODY_TYPE_INDEX]
        if bodyType in ('a', 'b', 'c'):
            return bodyType
        else:
            notify.error('invalid body type for suit %s: %s' % (name, bodyType))
        
def getSuitDept(name):
    if name in SuitGlobals.suitProperties:
        dept = SuitGlobals.suitProperties[name][SuitGlobals.DEPT_INDEX]
        if dept not in suitDepts:
            notify.error('invalid dept type for suit %s: %s' % (name, dept))
        else:
            return dept

